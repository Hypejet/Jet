package net.hypejet.jet.world.difficulty;

/**
 * Represents a Minecraft difficulty.
 *
 * @since 1.0
 * @author Codestech
 */
public enum Difficulty {
    /**
     * A peaceful difficulty.
     *
     * @since 1.0
     */
    PEACEFUL,
    /**
     * An easy difficulty.
     *
     * @since 1.0
     */
    EASY,
    /**
     * A normal difficulty.
     *
     * @since 1.0
     */
    NORMAL,
    /**
     * A hard difficulty.
     *
     * @since 1.0
     */
    HARD
}